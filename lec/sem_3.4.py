count = 0
for i in range(0, len(arr)+1): # идем от 1 до последнего. лен это длина. с 1 т.к. мы сравниваем с предыдущего i-1
    if(arr[i] < arr[i+1]): #сравниваем с предыдущим
        count+=1
print(count)

